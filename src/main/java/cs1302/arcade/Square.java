package cs1302.arcade;

import cs1302.arcade.Piece;

public class Square {
    private Piece piece;
    private int x;
    private int y;

    public Piece getPiece() {
        return this.Piece;
    } // getPiece

    public int getX() {
        return this.x;
    } // getX

    public int getY() {
        return this.y;
    } // getY

    public void setPiece(Piece newPiece) {
        this.piece = newPiece;
    } // setPiece

    public int setX(int newX) {
        this.x = newX;
    } // setX

    public int setY(int newY) {
        this.y = newY;
    } // setY

} // Square
